void pcd8544Setup()
{
	PORTB |= 0x10; //set reset pin high
	DDRB |= 0x3F; //Enable pins required for output (SCK, CE, DC, MISO, MOSI, RST)
	
	//Enable SPI in master mode at 1Mhz @ 8Mhz system clock
	SPCR |= (1 << SPI2X) | (1 << SPE) | (1 << MSTR) | (1 << SPR0);
	
	// Reset LCD sequence before command per Datasheet
	PORTB &= 0x77;
    _delay_ms(1);
	PORTB |=  0x10;
	
	// initialization commands
	spiWriteLCDCommand(0x21);
	spiWriteLCDCommand(0x06);
	spiWriteLCDCommand(0x13);
	spiWriteLCDCommand(0xC2);
	spiWriteLCDCommand(0x20);
	spiWriteLCDCommand(0x0C);
}


void spiWriteLCDCommand(uint8_t data)
{
  PORTB &= 0xF9; //set CE and DC low
  SPDR = data;
  while (!(SPSR & (1 << SPIF)));
  PORTB |= 0x06; //set CE and DC high
}


void spiWriteByte(uint8_t data, uint8_t chipEnPin)//chipEnPin on PORTB (General SPI use)
{
  //PORTB &= 0xFB; // set CE low
  PORTB &= ~(1 << chipEnPin);
  SPDR = data;
  while (!(SPSR & (1 << SPIF)));
  PORTB |= 0x06; //set CE and DC high
}

void clearLCD()
{
  for(unsigned int i = 0; i < bufferSize; i++) // Write zeros into lcdBuffer
  {
	lcdBuffer[i] = 0;
  }
}

void cursorPos(uint8_t x, uint8_t y)
{
	if ((x < maxRow) && (y < (maxCol / 6)))
	{
		uint8_t xPos = 0x40 | x;
		uint8_t yPos = 0x80 | (y*6);
		spiWriteLCDCommand(0x20);
		spiWriteLCDCommand(xPos);
		spiWriteLCDCommand(yPos);
		currLcdBufferIndex = (x * maxCol) + (y * 6);
	}
}

void writeChar(char character)
{
	if(currLcdBufferIndex >= 5)
	{
		cursorPos(0,0);
	}
	

	if ((character > 0x20) && (character < 0x7f))
	{
		uint8_t asciiIndexOffset = 0;
		for (uint8_t i = 0; i < 5; i++)
		{
			asciiIndexOffset = character - 32;
			lcdBuffer[currLcdBufferIndex] = pgm_read_byte(&charset[asciiIndexOffset][i]);
			currLcdBufferIndex++;
		}
		currLcdBufferIndex++;

	}
}

void writeString(char *str)
{
	uint8_t i = 0;
	while (str[i] != '\0')
	{
		writeChar(str[i]);
		i++;
	}
}

void updateLCD()
{
  //Write lcd buffer to LCD
  cursorPos(0, 0);
  for (unsigned int i = 0; i < bufferSize; i++) 
  {
	spiWriteByte(lcdBuffer[i], PB2);
  }  
}